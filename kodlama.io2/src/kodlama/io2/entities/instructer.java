package kodlama.io2.entities;

public class instructer {

	private String ad;
	private int age;
	
	public instructer() {
		
	}

	public instructer(String ad, int age) {
		this.ad = ad;
		this.age = age;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	
	
	
	
}
